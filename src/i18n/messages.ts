export type MessageSet = typeof zhCN

const searchEngineLabels = {
  'zh-CN': {
    google: 'Google',
    bing: 'Bing',
    baidu: '百度',
  },
  en: {
    google: 'Google',
    bing: 'Bing',
    baidu: 'Baidu',
  },
} as const

export function normalizeLanguage(value?: string | null): Language {
  return value === 'en' ? 'en' : 'zh-CN'
}

export function resolveInitialLanguage(): Language {
  if (typeof window === 'undefined') {
    return DEFAULT_LANGUAGE
  }

  return normalizeLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY))
}

export function persistLanguage(language: Language) {
  if (typeof window === 'undefined') {
    return
  }

  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language)
}

export function getMessages(language: Language): MessageSet {
  return messages[language]
}

export function getLocalizedSearchEngineName(
  language: Language,
  engine: Pick<SearchEngineConfig, 'id' | 'name'> | Pick<SearchEngineOption, 'id' | 'name'>
) {
  return (
    searchEngineLabels[language][engine.id as keyof (typeof searchEngineLabels)[Language]] ??
    engine.name
  )
}
